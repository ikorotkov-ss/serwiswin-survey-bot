def get_questions_for_role(role):
    """all = universal questions for everyone, returns questions for given role"""
    if role == "all":
        return [q for q in questions if q["role"] == "all"]
    return [q for q in questions if q["role"] in ("all", role)]


def format_questions(role):
    """Return a readable text block of questions for a given role."""
    qs = get_questions_for_role(role)
    current_block = None
    lines = []
    for q in qs:
        if q["block"] != current_block:
            current_block = q["block"]
            lines.append(f"\n--- {current_block} ---")
        lines.append(f"{q['number']}. {q['text']}")
    return "\n".join(lines)


